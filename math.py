import math

num = math.sqrt(16) 
print(num) # Output: 4.0

math.pi = 3.14159
math.e = 2.71828

num2 = math.pow(2, 3) # power of 2 raised to 3
print(num2) # Output: 8.0

num3 = math.log(100, 10) # logarithm base 10 of 100
print(num3) # Output: 2.0

num4 = math.sin(math.pi / 2) # sine of 90 degrees
print(num4) # Output: 1.0

num5 = math.factorial(5) # factorial of 5
print(num5) # Output: 120

num6 = math.ceil(4.2) # ceiling of 4.2
print(num6) # Output: 5

num7 = math.floor(4.8) # floor of 4.8
print(num7) # Output: 4 

num8 = math.gcd(48, 18) # greatest common divisor of 48 and 18
print(num8) # Output: 6

num9 = math.isqrt(16) # integer square root of 16
print(num9) # Output: 4

num10 = math.radians(180) # convert degrees to radians
print(num10) # Output: 3.141592653589793

num11 = math.degrees(math.pi) # convert radians to degrees
print(num11) # Output: 180.0



